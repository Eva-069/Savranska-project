import math                                # підключаємо модуль math для sin, cos
import numpy as np                         # підключаємо numpy для масивів значень
import matplotlib
matplotlib.use('Agg')                      # використовуємо бекенд без графічного вікна
import matplotlib.pyplot as plt            # підключаємо matplotlib для побудови графіка
import os                                  # підключаємо os для відкриття файлу

student_name = "Савранська Єва"            # змінна з прізвищем студента
student_group = "231"                      # змінна з номером групи

print("=" * 50)                            # виводимо роздільник
print(f" Студент: {student_name} | Група: {student_group}")  # виводимо інформацію про студента
print("=" * 50)                            # ще один роздільник

g = 9.81                                   # прискорення вільного падіння (м/с^2)

v0    = float(input("\nВведіть початкову швидкість (м/с): "))  # зчитуємо початкову швидкість
theta = float(input("Введіть кут кидання (градуси): "))        # зчитуємо кут кидання в градусах

theta_rad = math.radians(theta)            # перетворюємо градуси в радіани для math функцій

vx = v0 * math.cos(theta_rad)             # горизонтальна складова швидкості (м/с)
vy = v0 * math.sin(theta_rad)             # вертикальна складова швидкості (м/с)

t_flight = 2 * vy / g                     # повний час польоту: t = 2*V0*sin(θ) / g (с)
range_flight = vx * t_flight              # дальність польоту: x = Vx * t (м)
max_height = (vy ** 2) / (2 * g)          # максимальна висота: h = Vy^2 / (2*g) (м)

print(f"\n📐 Результати розрахунку:")       # заголовок блоку результатів
print(f"  Час польоту        : {t_flight:.2f} с")     # виводимо час польоту
print(f"  Максимальна висота : {max_height:.2f} м")   # виводимо максимальну висоту
print(f"  Дальність польоту  : {range_flight:.2f} м") # виводимо дальність польоту

t_values = np.linspace(0, t_flight, 300)              # масив моментів часу від 0 до t_flight
x_values = vx * t_values                              # горизонтальні координати: x = Vx * t
y_values = vy * t_values - 0.5 * g * t_values ** 2   # вертикальні координати: y = Vy*t - 0.5*g*t^2

plt.figure(figsize=(10, 5))                           # створюємо фігуру розміром 10×5 дюймів
plt.plot(x_values, y_values,                          # будуємо лінію траєкторії
         color="#2196F3", linewidth=2,                # синій колір, товщина 2
         label="Без опору повітря")                   # підпис для легенди

C_d = 0.6                                  # коефіцієнт опору для тенісного м'яча
rho = 1.225                                # густина повітря (кг/м^3)
A   = 0.00342                              # площа поперечного перерізу тенісного м'яча (м^2)
m   = 0.057                                # маса тенісного м'яча (кг)

dt  = 0.01                                 # крок часу для числового інтегрування (с)
x_air, y_air = 0.0, 0.0                    # початкові координати (м)
vx_air, vy_air = vx, vy                   # початкові складові швидкості (м/с)
xs, ys = [x_air], [y_air]                  # списки координат для графіка

while y_air >= 0:                          # симулюємо рух поки м'яч не впаде на землю
    v = math.sqrt(vx_air**2 + vy_air**2)  # поточна швидкість (м/с)
    F_drag = 0.5 * C_d * rho * A * v**2   # сила опору повітря (Н)
    ax_air = -F_drag * vx_air / (m * v)   # прискорення по x від опору (м/с^2)
    ay_air = -g - F_drag * vy_air / (m * v)  # прискорення по y від гравітації та опору
    vx_air += ax_air * dt                  # оновлюємо горизонтальну швидкість
    vy_air += ay_air * dt                  # оновлюємо вертикальну швидкість
    x_air  += vx_air * dt                  # оновлюємо горизонтальну координату
    y_air  += vy_air * dt                  # оновлюємо вертикальну координату
    xs.append(x_air)                       # додаємо x до списку
    ys.append(y_air)                       # додаємо y до списку

plt.plot(xs, ys,                           # будуємо лінію траєкторії з опором
         color="#FF5722", linewidth=2,     # помаранчевий колір
         linestyle="--",                   # пунктирна лінія
         label="З опором повітря")         # підпис для легенди

plt.title(f"Траєкторія польоту | {student_name} | V₀={v0} м/с, θ={theta}°",
          fontsize=12)                     # заголовок графіка з параметрами
plt.xlabel("Дистанція (м)")               # підпис осі X
plt.ylabel("Висота (м)")                  # підпис осі Y
plt.legend()                              # відображаємо легенду
plt.grid(True, alpha=0.3)                 # додаємо прозору сітку
plt.tight_layout()                        # автоматично підганяємо розміри
plt.savefig("trajectory.png", dpi=150)    # зберігаємо графік у файл PNG

print("\n✅ Графік збережено: trajectory.png")  # повідомлення про збереження
os.startfile("trajectory.png")                 # відкриваємо файл стандартною програмою Windows
print("=" * 50)                                # фінальний роздільник
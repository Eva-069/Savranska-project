import numpy as np                       # підключаємо бібліотеку NumPy для роботи з масивами

student_name = "Савранська Єва"          # змінна з прізвищем студента
student_group = "231"                    # змінна з номером групи

print("=" * 40)                          # виводимо роздільник для оформлення
print(f" Студент: {student_name} | Група: {student_group}")  # виводимо інформацію про студента
print("=" * 40)                          # ще один роздільник

# --- Створення масиву ---

np.random.seed(42)                       # фіксуємо генератор випадкових чисел для відтворюваності результату
array = np.random.randint(10, 100, size=15)  # створюємо масив із 15 випадкових цілих чисел від 10 до 99

print(f"\nМасив із 15 чисел:")           # заголовок перед виводом масиву
print(array)                             # виводимо сам масив на екран

# --- Обчислення статистики ---

minimum = array.min()                    # знаходимо мінімальне значення в масиві
maximum = array.max()                    # знаходимо максимальне значення в масиві
average = round(float(array.mean()), 2) # обчислюємо середнє арифметичне, округлюємо до 2 знаків
std_dev = round(float(array.std()), 2)  # обчислюємо стандартне відхилення, округлюємо до 2 знаків

# --- Виведення результатів ---

print("\n📊 Статистика масиву:")          # заголовок блоку статистики
print(f"  Мінімальне значення : {minimum}")   # виводимо мінімум
print(f"  Максимальне значення: {maximum}")   # виводимо максимум
print(f"  Середнє значення    : {average}")   # виводимо середнє
print(f"  Стандартне відхилення: {std_dev}")  # виводимо стандартне відхилення

print("\n💡 Що означає стандартне відхилення:")       # пояснення для звіту
print(f"  Числа в середньому відхиляються від")       # пояснення рядок 1
print(f"  середнього ({average}) на ±{std_dev} одиниць")  # пояснення рядок 2

print("\n" + "=" * 40)                   # фінальний роздільник
student_name = "Савранська Єва"          # змінна з прізвищем студента
student_group = "231"                    # змінна з номером групи

print("=" * 40)                          # виводимо роздільник для оформлення
print(f" Студент: {student_name} | Група: {student_group}")  # виводимо інформацію про студента
print("=" * 40)                          # ще один роздільник

# --- Читання чисел з файлу ---

with open("numbers.txt", "r") as file:   # відкриваємо файл numbers.txt для читання
    content = file.read()                # зчитуємо весь вміст файлу в рядок

numbers = list(map(int, content.split()))  # розбиваємо рядок по пробілах і перетворюємо в список цілих чисел

print(f"Зчитано чисел: {len(numbers)}")  # виводимо кількість зчитаних чисел
print(f"Числа: {numbers}")               # виводимо самі числа

# --- Обчислення статистики ---

minimum = min(numbers)                   # знаходимо найменше число у списку
maximum = max(numbers)                   # знаходимо найбільше число у списку
average = round(sum(numbers) / len(numbers), 2)  # рахуємо середнє: сума / кількість, округлюємо до 2 знаків

print(f"\nМінімальне: {minimum}")        # виводимо мінімум на екран
print(f"Максимальне: {maximum}")         # виводимо максимум на екран
print(f"Середнє: {average}")            # виводимо середнє на екран

# --- Запис результатів у новий файл ---

with open("stats.txt", "w") as file:     # відкриваємо (або створюємо) файл stats.txt для запису
    file.write(f"Студент: {student_name} | Група: {student_group}\n")  # записуємо ім'я студента
    file.write("=" * 35 + "\n")          # записуємо роздільник
    file.write(f"Мінімальне: {minimum}\n")   # записуємо мінімальне число
    file.write(f"Максимальне: {maximum}\n")  # записуємо максимальне число
    file.write(f"Середнє: {average}\n")      # записуємо середнє арифметичне

print("\n✅ Результати збережено у файл stats.txt")  # повідомляємо що файл створено
print("=" * 40)                          # фінальний роздільник
import pandas as pd                       # підключаємо бібліотеку pandas для роботи з таблицями

student_name = "Савранська Єва"           # змінна з прізвищем студента
student_group = "231"                     # змінна з номером групи

print("=" * 50)                           # виводимо роздільник для оформлення
print(f" Студент: {student_name} | Група: {student_group}")  # виводимо інформацію про студента
print("=" * 50)                           # ще один роздільник

# --- Читання CSV файлу ---

df = pd.read_csv("sales.csv")             # зчитуємо файл sales.csv у DataFrame (таблицю)

print("\n📋 Вихідна таблиця:")            # заголовок перед виводом таблиці
print(df)                                 # виводимо всю таблицю на екран

# --- Крок 1: Додаємо стовпець виручки ---

df["revenue"] = df["qty"] * df["price"]  # рахуємо виручку по кожному замовленню: кількість × ціна

print("\n💰 Таблиця з виручкою (стовпець revenue):")  # заголовок
print(df)                                 # виводимо оновлену таблицю з новим стовпцем

# --- Крок 2: Series сумарної виручки по регіонах ---

revenue_by_region = df.groupby("region")["revenue"].sum()  # групуємо по регіону і сумуємо виручку
revenue_by_region = revenue_by_region.sort_values(ascending=False)  # сортуємо спадно (від більшого до меншого)

print("\n📊 Сумарна виручка по регіонах (спадно):")  # заголовок
print(revenue_by_region)                  # виводимо Series з виручкою по регіонах

# --- Крок 3: Топ-3 регіони і фільтрація ---

top3_regions = revenue_by_region.head(3).index  # беремо індекси (назви) трьох найприбутковіших регіонів

print(f"\n🏆 Топ-3 регіони: {list(top3_regions)}")  # виводимо назви топ-3 регіонів

df_top3 = df[df["region"].isin(top3_regions)]  # залишаємо лише рядки де регіон входить у топ-3

print("\n✅ Замовлення лише з топ-3 регіонів:")  # заголовок фінальної таблиці
print(df_top3.to_string(index=False))     # виводимо відфільтровану таблицю без індексів

print("\n" + "=" * 50)                    # фінальний роздільник
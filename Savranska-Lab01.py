student_name = "Савранська Єва"          # змінна з прізвищем студента, який виконує роботу
student_group = "231"                    # змінна з номером групи студента

# Словник: ключ — ім'я студента, значення — список оцінок з трьох предметів
students = {
    "Олексій Мельник":   [85, 90, 78],   # оцінки Олексія з трьох предметів
    "Софія Бондаренко":  [60, 55, 72],   # оцінки Софії з трьох предметів
    "Дмитро Іванченко":  [91, 88, 95],   # оцінки Дмитра з трьох предметів
    "Анна Захаренко":    [65, 48, 70],   # оцінки Анни з трьох предметів
    "Микола Петренко":   [73, 80, 69],   # оцінки Миколи з трьох предметів
}

print("=" * 45)                          # виводимо рядок-роздільник для оформлення
print(f"  Студент: {student_name} | Група: {student_group}")  # виводимо прізвище і групу
print("=" * 45)                          # ще один роздільник

# --- Обчислення середнього балу кожного студента ---

averages = {}  # порожній словник для збереження середніх балів

for name, grades in students.items():    # перебираємо кожного студента у словнику
    avg = sum(grades) / len(grades)      # рахуємо середній бал: сума / кількість
    averages[name] = round(avg, 2)       # зберігаємо результат, округлений до 2 знаків
    print(f"{name}: оцінки {grades} → середній бал: {averages[name]}")  # виводимо результат

print("-" * 45)  # роздільник між блоками виводу

# --- Визначення найкращого студента ---

best_student = max(averages, key=averages.get)  # знаходимо ім'я з найвищим середнім балом
print(f"\n🏆 Найкращий студент: {best_student} ({averages[best_student]} балів)")  # виводимо переможця

# --- Студенти з середнім балом нижче 70 ---

print("\n⚠️  Студенти з середнім балом нижче 70:")  # заголовок для слабких студентів

found = False  # прапорець: чи знайдено хоч одного такого студента

for name, avg in averages.items():       # перебираємо всі середні бали
    if avg < 70:                         # перевіряємо умову: середній бал менший за 70
        print(f"  - {name}: {avg}")      # якщо умова виконана — виводимо ім'я і бал
        found = True                     # позначаємо, що знайдено студента

if not found:                            # якщо жодного не знайдено
    print("  Таких студентів немає.")    # виводимо відповідне повідомлення

print("=" * 45)  # фінальний роздільник
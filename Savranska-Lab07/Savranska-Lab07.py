import sqlite3                            # підключаємо вбудований модуль для роботи з SQLite
import pandas as pd                       # підключаємо pandas для роботи з DataFrame
import os                                 # підключаємо os для роботи зі змінними середовища

student_name = "Савранська Єва"           # змінна з прізвищем студента
student_group = "231"                     # змінна з номером групи

print("=" * 50)                           # виводимо роздільник для оформлення
print(f" Студент: {student_name} | Група: {student_group}")  # виводимо інформацію про студента
print("=" * 50)                           # ще один роздільник

# ============================================================
# ЗАВДАННЯ 1: Параметризований SELECT у SQLite + pandas
# ============================================================

print("\n📦 ЗАВДАННЯ 1: SQLite + pandas")  # заголовок першого завдання

# --- Крок 1: Створення in-memory SQLite БД ---

conn = sqlite3.connect(":memory:")        # створюємо базу даних у пам'яті (не зберігається на диск)
cursor = conn.cursor()                    # створюємо курсор для виконання SQL-запитів

# --- Крок 2: Створення таблиці та додавання записів ---

cursor.execute("""                        
    CREATE TABLE customers (
        id   INTEGER PRIMARY KEY,
        name TEXT NOT NULL,
        city TEXT NOT NULL
    )
""")                                      # створюємо таблицю customers з полями id, name, city

customers_data = [                        # список записів для додавання до таблиці
    (1, "Олена Коваль",    "Київ"),       # перший запис: id, ім'я, місто
    (2, "Дмитро Мельник",  "Львів"),      # другий запис
    (3, "Софія Бондар",    "Київ"),       # третій запис
    (4, "Андрій Іванченко","Одеса"),      # четвертий запис
    (5, "Марія Захаренко", "Львів"),      # п'ятий запис
]

cursor.executemany(                       
    "INSERT INTO customers VALUES (?, ?, ?)",  # SQL-запит із плейсхолдерами ? для безпечної вставки
    customers_data                        # передаємо список записів
)
conn.commit()                             # підтверджуємо зміни в базі даних

print("\n✅ Таблицю customers створено і заповнено")  # повідомлення про успішне створення

# --- Крок 3: Параметризований запит за містом ---

search_city = "Київ"                      # змінна з назвою міста для пошуку (плейсхолдер)

cursor.execute(
    "SELECT name FROM customers WHERE city = ?",  # параметризований SELECT із плейсхолдером
    (search_city,)                        # передаємо значення змінної як кортеж
)

rows = cursor.fetchall()                  # отримуємо всі рядки результату запиту

print(f"\n🔍 Пошук клієнтів у місті: {search_city}")  # виводимо назву міста пошуку
print(f"   Знайдено записів: {len(rows)}")             # виводимо кількість знайдених записів

# --- Крок 4: Завантаження результату у DataFrame ---

df_city = pd.DataFrame(rows, columns=["name"])         # створюємо DataFrame з результатів запиту
df_city.index = df_city.index + 1                      # нумерація з 1 замість 0

print(f"\n📋 DataFrame клієнтів з міста '{search_city}':")  # заголовок таблиці
print(df_city)                                         # виводимо DataFrame на екран

# --- Додатково: вся таблиця у DataFrame ---

df_all = pd.read_sql_query("SELECT * FROM customers", conn)  # зчитуємо всю таблицю через pandas
print("\n📋 Всі клієнти (повна таблиця):")             # заголовок повної таблиці
print(df_all.to_string(index=False))                   # виводимо без індексів

conn.close()                                           # закриваємо з'єднання з базою даних

# ============================================================
# ЗАВДАННЯ 2: PostgreSQL DSN без секретів у коді
# ============================================================

print("\n" + "=" * 50)                    # роздільник між завданнями
print("🐘 ЗАВДАННЯ 2: PostgreSQL DSN")    # заголовок другого завдання

# --- Крок 1: Задаємо змінні середовища (імітація .env файлу) ---

os.environ["PGUSER"]     = "eva_savranska"   # встановлюємо змінну середовища з іменем користувача
os.environ["PGPASSWORD"] = "secret123"       # встановлюємо змінну середовища з паролем
os.environ["PGHOST"]     = "localhost"       # встановлюємо змінну середовища з хостом
os.environ["PGDATABASE"] = "university_db"   # встановлюємо змінну середовища з назвою бази

# --- Крок 2: Зчитуємо змінні та збираємо DSN ---

pg_user = os.environ.get("PGUSER")          # зчитуємо ім'я користувача зі змінних середовища
pg_password = os.environ.get("PGPASSWORD")  # зчитуємо пароль зі змінних середовища
pg_host = os.environ.get("PGHOST")          # зчитуємо хост зі змінних середовища
pg_database = os.environ.get("PGDATABASE")  # зчитуємо назву бази зі змінних середовища

dsn_full = f"postgresql+psycopg://{pg_user}:{pg_password}@{pg_host}/{pg_database}"  # повний DSN з паролем
dsn_safe = f"postgresql+psycopg://{pg_user}:***@{pg_host}/{pg_database}"            # DSN без пароля для виводу

# --- Крок 3: Виводимо DSN без пароля ---

print(f"\n✅ DSN сформовано успішно")              # повідомлення про успіх
print(f"🔒 DSN (без пароля): {dsn_safe}")          # виводимо безпечний DSN без пароля
print(f"ℹ️  Пароль прихований з міркувань безпеки") # пояснення чому пароль прихований

print("\n" + "=" * 50)                             # фінальний роздільник
print(f" Роботу виконала: {student_name} | Група: {student_group}")  # підпис студента
print("=" * 50)                                    # фінальний роздільник
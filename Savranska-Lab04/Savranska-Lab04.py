import numpy as np                        # підключаємо бібліотеку NumPy для роботи з масивами

student_name = "Савранська Єва"           # змінна з прізвищем студента
student_group = "231"                     # змінна з номером групи

print("=" * 45)                           # виводимо роздільник для оформлення
print(f" Студент: {student_name} | Група: {student_group}")  # виводимо інформацію про студента
print("=" * 45)                           # ще один роздільник

# --- Створення матриці ---

np.random.seed(42)                        # фіксуємо генератор для відтворюваності результату
matrix = np.random.randint(10, 100, size=(6, 4))  # створюємо матрицю 6 рядків × 4 стовпці, числа від 10 до 99

print("\n📋 Матриця 6×4:")                # заголовок перед виводом матриці
print(matrix)                             # виводимо матрицю на екран

# --- Загальна статистика (без циклів — векторизовано) ---

minimum = matrix.min()                    # мінімальне значення серед усіх елементів матриці
maximum = matrix.max()                    # максимальне значення серед усіх елементів матриці
average = round(float(matrix.mean()), 2) # середнє арифметичне всіх елементів матриці

print("\n📊 Загальна статистика:")         # заголовок блоку статистики
print(f"  Мінімальне значення : {minimum}")    # виводимо мінімум
print(f"  Максимальне значення: {maximum}")    # виводимо максимум
print(f"  Середнє значення    : {average}")    # виводимо середнє

# --- Суми по рядках і стовпцях (векторизовано) ---

row_sums = matrix.sum(axis=1)             # сума елементів кожного рядка (axis=1 — по горизонталі)
col_sums = matrix.sum(axis=0)             # сума елементів кожного стовпця (axis=0 — по вертикалі)

print("\n➡️  Сума елементів у кожному рядку:")   # заголовок для сум рядків
for i, s in enumerate(row_sums):                 # перебираємо суми рядків з їх номерами
    print(f"  Рядок {i+1}: {s}")                 # виводимо номер рядка та його суму

print("\n⬇️  Сума елементів у кожному стовпці:")  # заголовок для сум стовпців
for j, s in enumerate(col_sums):                 # перебираємо суми стовпців з їх номерами
    print(f"  Стовпець {j+1}: {s}")              # виводимо номер стовпця та його суму

# --- Індекс максимального елемента ---

flat_index = matrix.argmax()              # знаходимо індекс максимуму у "плаский" версії масиву
row_idx, col_idx = np.unravel_index(flat_index, matrix.shape)  # перетворюємо в індекс рядка і стовпця

print(f"\n🎯 Максимальний елемент: {maximum}")             # виводимо значення максимуму
print(f"   Знаходиться у рядку {row_idx+1}, стовпці {col_idx+1}")  # виводимо його позицію

print("\n" + "=" * 45)                    # фінальний роздільник